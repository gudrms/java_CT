package 문제;

public class 최소직사각형10 {
    public int solution(int[][] sizes) {
        // 1. 가로와 세로 중 각각의 최댓값을 저장할 변수 초기화
        int maxWidth = 0;
        int maxHeight = 0;

        // 2. 모든 명함을 하나씩 확인
        for (int[] size : sizes) {
            // 명함의 가로(w)와 세로(h) 중 더 긴 쪽을 w로, 짧은 쪽을 h로 정함
            // (명함을 회전시켜서 항상 긴 쪽을 가로에 맞춘다고 가정하는 것)
            int w = Math.max(size[0], size[1]);
            int h = Math.min(size[0], size[1]);

            // 3. 지금까지 확인한 명함들 중 가장 큰 가로 길이와 세로 길이를 갱신
            // 모든 명함을 수납하려면 가로는 '긴 쪽들 중 최대', 세로는 '짧은 쪽들 중 최대'가 되어야 함
            maxWidth = Math.max(maxWidth, w);
            maxHeight = Math.max(maxHeight, h);
        }

        // 4. 지갑의 크기(넓이) 반환
        return maxWidth * maxHeight;
    }
}


